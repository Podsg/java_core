package Start.lesson3;

public class Primitives {
    public static void main(String[] args) {
        //Логические значения
        boolean answerYuri = true;
        boolean answerPetr = false;

        //Целые числа
        //Byte
        byte maxByte = 127;
        byte minByte = -128;

        //Short - короткое число, состоит из дву байт, или из 16 битов
        short maxShort = 32_767;
        short minShort = -32_768;

        //Integer - целое число, которое состоит из 4 байт, или из 32 бит
        int maxInt = 2_147_483_647;
        int minInt = -2_147_483_648;

        //Long -целое число, которое состоит из 8 байт или 64 бит
        long maxLong = 9_223_372_036_854_775_807L;
        long minLong = -9_223_372_036_854_775_808L;

        //Double - число с плавающей точко, 64 bit
        double myBottle = 1.5;
        //Float - число с плавающей точкой 32 bit
        float cola = 0.33F;
    }
}
